package org.masterjung.dto.join;

public class UserCountDto {
	private String date;
	private int cnt;
	
	
	public UserCountDto() {}

	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	
	
}
